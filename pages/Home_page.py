from playwright.sync_api import Page

class HomePage:
    def __init__(self, page: Page):
        self.page = page

    def check_project_name(self):
        self.page.get_by_role("link", name="Projects").click()
        self.page.get_by_role("heading", name="Rate analysis of furniture").locator("span").click()
        self.page.get_by_role("cell", name="Q001").locator("a").click()
        self.page.get_by_text("Archivista interiors", exact=True).click()
        a1 = self.page.text_content("//label[text()='Archivista interiors']")
        a = "Archivista interiors"
        assert a == a1


